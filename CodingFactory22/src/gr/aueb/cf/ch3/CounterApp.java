package gr.aueb.cf.ch3;

/*
* Εκτελεί ένα συγκεκριμένο αριθμό επαναλήψεων
*/
public class CounterApp {
    public static void main(String[] args) {
        // Δήλωση και αρχικοποίηση μεταβλητών
        int i = 1;

        // Εντολές & εκτύπωση αποτελεσμάτων
        while (i <= 10){
            System.out.println("*");
            i++;
        }
    }
}
