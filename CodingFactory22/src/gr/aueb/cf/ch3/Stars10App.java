package gr.aueb.cf.ch3;

/**
 * Εκτυπώνει 10 αστεράκια (10 φορές από ένα αστεράκι)
 */
public class Stars10App {

    public static void main(String[] args) {
        // Δήλωση και αρχικοποίηση μεταβλητών
        int i = 1;

        // Εντολές & Εκτύπωση αποτελεσμάτων
        while (i <= 10) {
            System.out.println("*");
            i++;
        }
    }
}
